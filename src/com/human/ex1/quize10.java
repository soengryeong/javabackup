package com.human.ex1;

public class quize10 {

	public static void main(String[] args) {
		// 간단한 카운트 프로그램을 제작해보자. 
		//60초부터 0초까지 숫자를 출력하고 0초가 되면 "발사"를 출력한다.
            int sum=0;
		for(int i =60; i>=sum; i--) {
			System.out.println(i);
			if(i==0) {
				System.out.println("발사");
			}
		}
       
	}

}
